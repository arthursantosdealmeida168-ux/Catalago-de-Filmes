import React from "react";
import { Search, X } from "lucide-react";
import { Input } from "@/components/ui/input";

export default function SearchInput({ value, onChange, placeholder = "Buscar filmes..." }) {
  return (
    <div className="relative w-full max-w-md">
      <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-muted-foreground" />
      <Input
        value={value}
        onChange={(e) => onChange(e.target.value)}
        placeholder={placeholder}
        className="pl-10 pr-10 bg-secondary border-border text-foreground placeholder:text-muted-foreground rounded-full font-inter h-11"
      />
      {value && (
        <button
          onClick={() => onChange("")}
          className="absolute right-3 top-1/2 -translate-y-1/2 text-muted-foreground hover:text-foreground transition-colors"
        >
          <X className="w-4 h-4" />
        </button>
      )}
    </div>
  );
}